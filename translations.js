// Translation utility
function t(key, lang = null) {
  const currentLang = lang || document.documentElement.getAttribute('lang') || 'en';
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

// Update all translatable elements
function updateTranslations(lang) {
  document.documentElement.setAttribute('lang', lang);
  
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = t(key, lang);
    
    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      element.placeholder = translation;
    } else if (element.innerHTML.includes('<br>')) {
      element.innerHTML = translation;
    } else {
      element.textContent = translation;
    }
  });
  
  // Save preference
  localStorage.setItem('intelimaris-lang', lang);
}

// Initialize language from localStorage or browser
function initLanguage() {
  const savedLang = localStorage.getItem('intelimaris-lang');
  const browserLang = navigator.language.split('-')[0];
  const supportedLangs = ['en', 'es', 'el'];
  
  let lang = 'en';
  if (savedLang && supportedLangs.includes(savedLang)) {
    lang = savedLang;
  } else if (supportedLangs.includes(browserLang)) {
    lang = browserLang;
  }
  
  updateTranslations(lang);
  
  // Update active state on language selector
  document.querySelectorAll('.lang-option').forEach(option => {
    option.classList.toggle('active', option.dataset.lang === lang);
  });
}

if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, t, updateTranslations, initLanguage };
}
